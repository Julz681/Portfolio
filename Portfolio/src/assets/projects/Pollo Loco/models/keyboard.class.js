/**
 * Class representing the state of keyboard keys.
 * Each property tracks whether a specific key is pressed (true) or not (false).
 */
class Keyboard {
  LEFT = false;   // Left arrow key
  RIGHT = false;  // Right arrow key
  UP = false;     // Up arrow key
  DOWN = false;   // Down arrow key
  SPACE = false;  // Spacebar
  D = false;      // 'D' key (e.g., throw action)
  P = false;      // 'P' key (e.g., pause)
  C = false;      // 'C' key (e.g., continue)
  SHIFT = false;  // Shift key (modifier)
  S = false;      // 'S' key (e.g., screenshot or other action)
  F = false;      // 'F' key (e.g., fullscreen toggle)
  M = false;      // 'M' key (mute)
  U = false;      // 'U' key (unmute)
}
